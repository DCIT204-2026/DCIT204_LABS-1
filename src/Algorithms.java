public class Algorithms{

    // Searches through the array element by element until the target is found
    // or the end of the array is reached. Returns the index if found, -1 otherwise.
    public static int linearSearch(int[] arr, int target) {
        for (int i = 0; i < arr.length; i++) {
            if (arr[i] == target) {
                return i;
            }
        }
        return -1;
    }

    // Searches for a target value in a SORTED array by repeatedly dividing
    // the search space in half. Returns the index if found, -1 otherwise.
    public static int binarySearch(int[] arr, int target) {
        int low = 0;
        int high = arr.length - 1;

        while (low <= high) {
            int mid = low + (high - low) / 2;

            if (arr[mid] == target) {
                return mid;
            } else if (arr[mid] < target) {
                low = mid + 1;
            } else {
                high = mid - 1;
            }
        }
        return -1;
    }
}



